        $skipped = 0;
        $errors  = 0;

        foreach ($rows as $row) {
            $fieldKey = $row->field_key ?? '';
            $langCode = $row->lang_code ?? 'en-GB';

            if ($fieldKey === '') {
                $skipped++;
                continue;
            }

            $exists = (int) $db->setQuery(
                $db->getQuery(true)->select('COUNT(*)')->from('#__aiboost_translations')
                    ->where($db->quoteName('field_key') . '=' . $db->quote($fieldKey))
                    ->where($db->quoteName('lang_code') . '=' . $db->quote($langCode))
            )->loadResult();

            if ($exists > 0) {
                $skipped++;
                continue;
            }

            try {
                $db->setQuery(
                    $db->getQuery(true)
                        ->insert('#__aiboost_translations')
                        ->columns(['field_key', 'lang_code', 'field_value', 'created_at', 'updated_at'])
                        ->values(
                            $db->quote($fieldKey) . ',' .
                            $db->quote($langCode) . ',' .
                            $db->quote($row->field_value ?? '') . ',' .
                            $db->quote($now) . ',' .
                            $db->quote($now)
                        )
                )->execute();
                $copied++;
            } catch (\Throwable $e) {
                $errors++;
                self::logEvent('warning', '[AiBoost] Migration error (translation ' . $fieldKey . '/' . $langCode . '): ' . $e->getMessage());
            }
        }

        if ($errors === 0) {
            $this->dropOldTable($db, $oldTable);
            Factory::getApplication()->enqueueMessage(
                sprintf(
                    'AI Boost: Translations migration complete — %d row(s) copied, %d skipped (already existed). Old table removed.',
                    $copied,
                    $skipped
                ),
                'message'
            );
        } else {
            Factory::getApplication()->enqueueMessage(
                sprintf(
                    'AI Boost: Translations migration — %d copied, %d skipped, %d error(s). Old table kept for safety; check error log.',
                    $copied,
                    $skipped,
                    $errors
                ),
                'warning'
            );
        }
    }

    private function dropOldTable(\Joomla\Database\DatabaseInterface $db, string $tableName): void
    {
        try {
            $db->setQuery('DROP TABLE IF EXISTS ' . $db->quoteName($tableName))->execute();
        } catch (\Throwable $e) {
            self::logEvent('warning', '[AiBoost] Could not drop old table ' . $tableName . ': ' . $e->getMessage());
            Factory::getApplication()->enqueueMessage(
                'AI Boost: Old table ' . $tableName . ' could not be removed automatically. You may drop it manually.',
                'notice'
            );
        }
    }

