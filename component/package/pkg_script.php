            'scraper_mj12bot', 'scraper_blexbot', 'scraper_rogerbot',
            'scraper_screamingfrog', 'scraper_sitebulb', 'scraper_siteauditor',
            'scraper_serpstatbot', 'scraper_bytespider', 'scraper_petalbot',
        ];

        try {
            $db = Factory::getDbo();

            // Skip if the new settings table does not yet exist
            $tables = $db->setQuery(
                'SHOW TABLES LIKE ' . $db->quote($db->getPrefix() . 'aiboost_settings')
            )->loadColumn();
            if (!in_array($db->getPrefix() . 'aiboost_settings', $tables, true)) {
                return;
            }

            $rows = $db->setQuery(
                $db->getQuery(true)
                    ->select(['setting_key', 'settings_json'])
