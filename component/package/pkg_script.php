    /**
     * Write or update static files that must live on disk.
     *
     * LiteSpeed / Apache serve static files directly (bypassing PHP), and
     * standard Joomla .htaccess excludes robots.txt from PHP rewriting:
     *   RewriteCond %{REQUEST_URI} !^/robots.txt
     * So robots.txt MUST be a physical file — we write it here.
     */
    private function cleanupStaticFiles(): void
    {
        $robotsFile = JPATH_ROOT . '/robots.txt';

        // Load current settings to build a proper robots.txt
        $settingsJson = '';
        try {
            $db           = Factory::getDbo();
            $settingsJson = (string) $db->setQuery(
                $db->getQuery(true)
                    ->select($db->quoteName('settings_json'))
                    ->from('#__aiboost_settings')
                    ->where($db->quoteName('setting_key') . '=' . $db->quote('main'))
            )->loadResult();
        } catch (\Throwable $e) {
            // Table may not exist yet on fresh install — use defaults
        }

        $settings = $settingsJson ? (json_decode($settingsJson, true) ?? []) : [];

        // Respect the master toggle — when robots.txt management is off we do
        // not touch the user's file here.
        if ((int) ($settings['enable_robots'] ?? 1) !== 1) {
            return;
        }

        $siteUrl = (string) (\Joomla\CMS\Uri\Uri::getInstance()->getScheme()
            . '://' . \Joomla\CMS\Uri\Uri::getInstance()->getHost());

        $existing = is_file($robotsFile) ? (string) @file_get_contents($robotsFile) : '';

        // Task #566 — inject ONLY our fenced block, preserving any user rules.
        // Prefer the canonical lib builder (installed under the component) so the
        // block matches what a settings save would produce; fall back to a
        // minimal inline block if the lib is not loadable during install.
        $builder = JPATH_ADMINISTRATOR . '/components/com_aiboost/lib/src/RobotsTxtBuilder.php';
        if (is_file($builder)) {
            require_once $builder;
        }

        if (class_exists('\\AiBoost\\Lib\\RobotsTxtBuilder')) {
            $content = \AiBoost\Lib\RobotsTxtBuilder::injectManagedBlock($existing, $settings, $siteUrl);
        } else {
            $block = $this->buildRobotsBlockFallback($settings, $siteUrl);
            $clean = rtrim($this->stripRobotsBlock($existing));
            $content = $clean === '' ? $block : $clean . "\n\n" . $block;
        }

        @file_put_contents($robotsFile, $content);
    }

