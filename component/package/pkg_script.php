    /**
     * Task #565 — perpetual activation backfill.
     *
     * Existing paid installs predate the `pro_activated` flag. Set it (once)
     * for any install that currently resolves to Pro — an active license_state
     * row, a legacy Pro `license_tier`, or any true `pro_skus` entry — so the
     * new single gate (PluginRegistry::isProActive) keeps them unlocked. Runs
     * unconditionally (its own guard) because migrateLicenseTierToProSkuMap()
     * early-returns on already-migrated installs and would otherwise skip this.
     * Never clears the flag.
     *
     * Task #567 — this backfill only sees LOCAL state. A lapsed past purchaser
     * whose license_state/license_tier/pro_skus markers were cleared slips
     * through here and is left Free. The runtime safety net for that case is
     * AiBoost\Lib\LicenseReconcile (admin-only, throttled): it asks the update
     * server, by install_id, whether this install was ever bound to a real
     * purchase and re-activates Pro perpetually if so.
     *
     * Package uninstall PRESERVES pro_activated and license_state (perpetual
     * activation survives uninstall by design — see uninstall()), so after an
     * uninstall → reinstall this backfill is normally a no-op. It exists for
     * legacy upgrades that predate the flag and for installs whose markers
     * were cleared outside our code (manual DB edits, pre-fix uninstalls).
     */
    private function migrateActivateProPerpetual(): void
    {
        try {
            $db = Factory::getDbo();
            $q  = $db->getQuery(true)
                ->select($db->quoteName('settings_json'))
                ->from($db->quoteName('#__aiboost_settings'))
                ->where($db->quoteName('setting_key') . ' = ' . $db->quote('main'));
            $db->setQuery($q, 0, 1);
            $raw = (string) ($db->loadResult() ?? '');
            if ($raw === '') {
                return; // Fresh install — nothing to activate.
            }
            $data = json_decode($raw, true);
            if (!is_array($data)) {
                return;
            }
            if ((string) ($data['pro_activated'] ?? '0') === '1') {
                return; // Already activated — never re-touch.
            }

            $resolvesPro = false;

            // 1. A verified active license_state row.
            if (isset($data['license_state']) && is_array($data['license_state'])) {
                foreach ($data['license_state'] as $st) {
                    if (is_array($st)
                        && trim((string) ($st['key'] ?? '')) !== ''
                        && strtolower((string) ($st['status'] ?? '')) === 'active'
                    ) {
                        $resolvesPro = true;
                        break;
                    }
                }
            }
            // 2. Legacy Pro tier.
            if (!$resolvesPro) {
                $tier = strtolower(trim((string) ($data['license_tier'] ?? 'free')));
                if (in_array($tier, ['pro', 'developer', 'agency', 'professional', 'basic'], true)) {
                    $resolvesPro = true;
                }
            }
            // 3. Legacy per-SKU map with any unlocked SKU.
            if (!$resolvesPro
                && isset($data['pro_skus']) && is_array($data['pro_skus'])
                && array_filter($data['pro_skus'])
            ) {
                $resolvesPro = true;
            }

            if (!$resolvesPro) {
                return; // Base-only install with no historical activation signal.
            }

            $data['pro_activated'] = '1';
            if (empty($data['pro_activated_at'])) {
                $data['pro_activated_at'] = gmdate('c');
            }
            if (empty($data['pro_activated_version'])) {
                $data['pro_activated_version'] = class_exists('AiBoost\\Version')
                    ? \AiBoost\Version::VERSION
                    : 'migrated';
            }

            $newJson = $this->encodeSettingsBlobSafe($data);
            if ($newJson === null) {
                self::logEvent('warning', '[AiBoost] migrateActivateProPerpetual: skipped write — re-encoding settings_json yielded an empty/invalid value; row left intact.');
                return;
            }
            $update = $db->getQuery(true)
                ->update($db->quoteName('#__aiboost_settings'))
                ->set($db->quoteName('settings_json') . ' = ' . $db->quote($newJson))
                ->where($db->quoteName('setting_key') . ' = ' . $db->quote('main'));
            $db->setQuery($update);
            $db->execute();
        } catch (\Throwable $e) {
            self::logEvent('warning', '[AiBoost] migrateActivateProPerpetual failed: ' . $e->getMessage());
        }
    }

    /**
     * Task #428 — migrate the legacy single `license_tier` flag inside
     * #__aiboost_settings.settings_json into a forward-compatible per-SKU
     * `pro_skus` structure. The old key is preserved for backward compat
     * with code that still reads it; new code should consult PluginRegistry.
     *
     * Idempotent: re-running yields the same JSON.
     */
    private function migrateLicenseTierToProSkuMap(): void
    {
        try {
            $db = Factory::getDbo();
            $q  = $db->getQuery(true)
                ->select($db->quoteName('settings_json'))
                ->from($db->quoteName('#__aiboost_settings'))
                ->where($db->quoteName('setting_key') . ' = ' . $db->quote('main'));
            $db->setQuery($q, 0, 1);
            $raw = (string) ($db->loadResult() ?? '');
            if ($raw === '') {
                return; // No settings row yet — nothing to migrate.
            }
            $data = json_decode($raw, true);
            if (!is_array($data)) {
                return;
            }
            // Task #429 — even if pro_skus is already populated (#428 migration),
            // we must still backfill license_state.bundle when missing so the
            // runtime gate honors legacy paid installs.
            $alreadyMigratedSkus  = isset($data['pro_skus']) && is_array($data['pro_skus']);
            $alreadyHasLicenseSt  = isset($data['license_state']) && is_array($data['license_state'])
                                  && !empty($data['license_state']);
            if ($alreadyMigratedSkus && $alreadyHasLicenseSt) {
                return; // Fully migrated under #429 — nothing to do.
            }

            $tier   = strtolower(trim((string) ($data['license_tier'] ?? 'free')));
            $proSkus = $alreadyMigratedSkus ? $data['pro_skus'] : [];
            $hasPro = in_array($tier, ['pro', 'developer', 'agency', 'professional', 'basic'], true)
                    || (bool) array_filter($proSkus);

            if (!$alreadyMigratedSkus) {
                $data['pro_skus'] = [
                    'schema'   => $hasPro,
                    'aeo'      => $hasPro,
                    'og'       => $hasPro,
                    'hreflang' => $hasPro,
                    'code'     => $hasPro,
                ];
            }

            // Task #429 — also materialise the new per-SKU license_state map
            // so PluginRegistry's runtime gate sees the legacy Pro install as
            // a bundle-level active license. Users can then enter real per-SKU
            // keys via Components → AI Boost → Licenses to refine the picture.
            if ($hasPro && empty($data['license_state'])) {
                $existingKey = trim((string) ($data['license_key'] ?? ''));
                $data['license_state'] = [
                    'bundle' => [
                        'key'         => $existingKey !== '' ? $existingKey : 'MIGRATED-LEGACY-0.41.0',
                        'status'      => 'active',
                        'expires_at'  => null,
                        'verified_at' => gmdate('c'),
                        'activations_remaining' => null,
                        'mock'        => false,
                        'message'     => 'Migrated from legacy license_tier on upgrade to 0.41.0.',
                    ],
                ];
                // Keep license_tier='pro' for plugins that still read it directly.
                $data['license_tier'] = 'pro';
            } else {
                // Free tier — drop the legacy flag entirely so the new
                // license_state path is the single source of truth.
                unset($data['license_tier']);
            }

            $newJson = $this->encodeSettingsBlobSafe($data);
            if ($newJson === null) {
                self::logEvent('warning', '[AiBoost] migrateLicenseTierToProSkuMap: skipped write — re-encoding settings_json yielded an empty/invalid value; row left intact.');
                return;
            }
            $update = $db->getQuery(true)
                ->update($db->quoteName('#__aiboost_settings'))
                ->set($db->quoteName('settings_json') . ' = ' . $db->quote($newJson))
                ->where($db->quoteName('setting_key') . ' = ' . $db->quote('main'));
            $db->setQuery($update);
            $db->execute();
        } catch (\Throwable $e) {
            self::logEvent('warning', '[AiBoost] migrateLicenseTierToProSkuMap failed: ' . $e->getMessage());
        }
    }

