use Joomla\CMS\Factory;

class Pkg_AiboostInstallerScript
{
    public const VERSION     = '0.50.2';
    // Build-injected (scripts/build-package-zip.py): the Free build keeps
    // `false`; the combined Pro-edition build flips this to `true`. Drives the
    // `pro_installed` marker + the one-time sweep of the legacy *_pro decorators.
    public const IS_PRO_EDITION = false;
    public const MIN_PHP     = '8.1.0';
    public const MIN_JOOMLA  = '5.0.0';

    /**
     * Task #566 — AI Boost owns ONLY a fenced block inside the user's
     * robots.txt. These markers MUST stay byte-identical with
     * AiBoost\Lib\RobotsTxtBuilder::{BEGIN,END}_MARKER and the legacy marker;
     * the installer/uninstaller cannot autoload the lib, so they are inlined.
     */
    private const ROBOTS_BEGIN_MARKER  = '# BEGIN AI Boost for Joomla managed block (aiboostnow.com) - do not edit between these markers';
    private const ROBOTS_END_MARKER    = '# END AI Boost for Joomla managed block';
    private const ROBOTS_LEGACY_MARKER = '# Managed by AI Boost';

    /**
     * Settings keys removed from the `main` #__aiboost_settings row on package
     * uninstall. ONLY developer/QA override keys may ever appear here —
     * perpetual activation survives uninstall by design, so the activation and
     * licence keys (pro_activated, pro_activated_at, pro_activated_version,
     * license_key, license_state, install_id) MUST NEVER be added to this
     * list. Wiping them would permanently relock a customer whose licence has
     * expired but who is perpetually activated — exactly who the activation
     * model protects (expiry only pauses updates/support, never the features).
     * Guarded by PkgScriptUninstallKeysTest.
     */
    public const UNINSTALL_WIPED_KEYS = [
        'dev_license_preview',
        'dev_force_free_tier',
        'license_simulation',
    ];

    /**
     * Strip the AI Boost managed block from existing robots.txt content,
     * preserving any user-authored rules. Mirrors
     * AiBoost\Lib\RobotsTxtBuilder::stripManagedBlock(). Returns '' when the
     * whole file was ours (current fenced-only or legacy pre-fence format).
     */
    private function stripRobotsBlock(string $existing): string
    {
        if ($existing === '') {
            return '';
        }

        // Legacy pre-fence install: the entire file was generated by us.
        if (str_starts_with(ltrim($existing, "\xEF\xBB\xBF \t\r\n"), self::ROBOTS_LEGACY_MARKER)) {
            return '';
        }

        $pattern = '/\n*' . preg_quote(self::ROBOTS_BEGIN_MARKER, '/')
            . '.*?' . preg_quote(self::ROBOTS_END_MARKER, '/') . '[^\n]*\n?/su';

        return (string) preg_replace($pattern, '', $existing);
    }

    /**
     * Task #511 — Route installer log events through the central Logger
     * when the AiBoost\Lib autoloader is already registered. During the
     * very first package install the lib autoloader is not yet wired in,
     * so fall back to native error_log() and keep the install path safe.
     */
    private static function logEvent(string $severity, string $msg): void
    {
        if (class_exists('AiBoost\\Lib\\Logger', false)) {
            try {
                \AiBoost\Lib\Logger::$severity($msg, ['source' => 'pkg_script']);
                return;
            } catch (\Throwable $e) {
                // fall through to error_log
            }
        }
        @error_log('[AiBoost][' . strtoupper($severity) . '] ' . $msg);
    }

    public function preflight(string $type, object $parent): bool
    {
        if (version_compare(PHP_VERSION, self::MIN_PHP, '<')) {
            Factory::getApplication()->enqueueMessage(
                sprintf('AI Boost requires PHP %s or higher. You are running PHP %s.', self::MIN_PHP, PHP_VERSION),
                'error'
            );
            return false;
        }

        if (version_compare(JVERSION, self::MIN_JOOMLA, '<')) {
            Factory::getApplication()->enqueueMessage(
                sprintf('AI Boost requires Joomla %s or higher. You are running Joomla %s.', self::MIN_JOOMLA, JVERSION),
                'error'
            );
            return false;
        }

        // Phase 6.4 — downgrade lock. A Pro-installed or perpetually-activated
        // site must never be silently downgraded to an OLDER package (e.g. the
        // Free JED ZIP installed over a paid site). The combined Pro package
        // ships no update server; this is the belt to that suspenders.
        if (in_array($type, ['install', 'update', 'discover_install'], true)) {
            $installed = $this->installedPackageVersion();
            if ($installed !== ''
                && version_compare(self::VERSION, $installed, '<')
                && $this->isProSiteByFlags()) {
                Factory::getApplication()->enqueueMessage(
                    sprintf(
                        'AI Boost: refusing to downgrade an activated/Pro install from %s to %s. '
                        . 'Please install the current or a newer package.',
                        $installed,
                        self::VERSION
                    ),
                    'error'
                );
                return false;
            }
        }

        return true;
    }

    public function postflight(string $type, object $parent): void
    {
        // Phase 0.1 — NEVER act on uninstall. Joomla invokes postflight() with
        // $type='uninstall' AFTER uninstall() has already preserved the data;
        // without this guard everything below (CREATE TABLE, the settings
        // migrations, the *_pro plugin re-enable) re-runs over a half-removed
        // filesystem and rewrites the customer's settings blob. Mirrors the
        // guard pkg_script_pro.php already uses. The narrower in_array() check
        // further down is now redundant but left in place.
        if (!in_array($type, ['install', 'update', 'discover_install'], true)) {
            return;
        }

        // Phase 0.3 — back up the live `main` settings blob BEFORE any migration
        // can touch it, so support can revert a corrupted upgrade with zero data
        // loss. No-op on a fresh install (no row yet). Never touches `main`.
        $this->backupMainSettings();

        // v0.7.0 — ensure new tables exist on upgrade
        $this->ensureNewTables();

        // Migration runs on both install and upgrade:
        //   • On upgrade from pkg_aiboost: idempotent upsert skips already-migrated rows.
        //   • On fresh install after running plg_system_joomlaboost: old tables are present
        //     and must be migrated even though this is technically an 'install' of the new
        //     package (users uninstall the old plugin ZIP, then install the new package).
        $this->runDataMigration();

        // v0.32.5 — migrate legacy aggregate `robots_block_scrapers` → per-bot scraper_* keys.
        // MUST run AFTER runDataMigration() so that rows copied from the legacy
        // #__joomlaboost_settings table are also canonicalized in the same postflight pass.
        $this->migrateRobotsBlockScrapers();
        $this->migrateSchemaAuthorEntity();
        // Task #428 — migrate legacy `license_tier` flag into per-SKU map.
        $this->migrateLicenseTierToProSkuMap();
        // Task #565 — perpetual activation backfill (MUST run AFTER the #428
        // migration so an active license_state is already materialised).
        $this->migrateActivateProPerpetual();
        // Task #438 — uninstall legacy aiboost_perf plugin (renamed to aiboost_core).
        $this->migrateAiboostPerfToCore();
        // Task #440 — ensure a stable install_id (UUIDv4) exists so the
        // license-heartbeat endpoint can detect domain/install collisions.
        $this->ensureInstallId();
        // Task #473 — default conflict_mode to "cooperative" so existing
        // installs adopt the new ConflictDetector-friendly behaviour.
        $this->ensureConflictModeDefault();
        // Task #482 — backfill page-level default policy and rewrite legacy
        // per-bot 'default' values to '' so the new radio UI is unambiguous.
        $this->migrateCrawlerDefaultPolicy();

        // Phase 6 — record the installed EDITION (Pro vs Free) into the `main`
        // blob BEFORE the isProInstall() gates below consult it. The build
        // injects IS_PRO_EDITION (Free=false, combined Pro=true). This keeps a
        // freshly-installed, not-yet-activated Pro buyer reading as Pro so their
        // OG fields are kept (ensureOgCustomFields), never removed.
        $this->setInstalledEdition(self::IS_PRO_EDITION);

        // Phase 6 — on the combined Pro edition, sweep the now-redundant legacy
        // *_pro decorator plugins + the old pkg_aiboost_pro add-on package. The
        // Pro logic was relocated into the 7 free plugins (already upgraded in
        // place by this package install), so the decorators are dead weight; a
        // Pro site should show ONLY the 7 single elements. Cascade-safe.
        if (self::IS_PRO_EDITION) {
            $this->sweepCollapsedProDecorators();
        }

        // Split model (Free base + Pro add-on package): only clean up ORPHANED
        // Pro/add-on extension rows on a genuinely Free install. When the Pro
        // package IS present (or a Pro licence is active), the *_pro plugins are
        // the current product and MUST be preserved — otherwise a Free-base
        // update would uninstall a paying customer's Pro and lock the whole UI
        // (Bojan's bug #8). Mirrors the !isProInstall() gating used below.
        if (!$this->isProInstall()) {
            $this->removeLegacySplitPackageArtifacts();
        }
        // v0.75.0 — Remove integration plugins that were part of abandoned dev
        // models (e.g. v1 two-plugin YOOtheme split). Runs unconditionally on
        // BOTH Free and Pro installs — these elements no longer exist in any
        // current package and must be removed regardless of licence state.
        $this->removeObsoleteIntegrationPlugins();
        // v0.76.6 — the admin Health dashboard module (mod_aiboost_health) is
        // pulled from the product for now (owner decision). Remove any copy a
        // previous Pro package installed, including the placed dashboard
        // instance, so it stops appearing in Extensions and on the dashboard.
        $this->removeHealthAdminModule();
        $this->enablePlugins();
        $this->enableIntegrationPlugins();

        if (in_array($type, ['install', 'update', 'discover_install'], true)) {
            // Task #566 — (re)generate static artifacts (robots.txt managed
            // block, llms.txt, sitemap) ONLY on a genuine install/update.
            // Calling this unconditionally re-injected the robots.txt block
            // during an uninstall-context postflight, defeating the uninstall
            // strip.
            $this->cleanupStaticFiles();

            // Legacy package split compatibility: add-on installs keep the
            // article OG fields available; base-only installs remove field rows
            // created by older add-on builds without touching user content.
            if ($this->isProInstall()) {
                $this->ensureOgCustomFields();
            } else {
                $this->removeOgCustomFieldsForFree();
                // The admin Health module now ships from the legacy add-on
                // package. Base-only updates remove leftover module instances
                // from older package layouts.
                $this->removeHealthModuleForFree();
            }

            // Keep the Components menu label normalized to the one-product name.
            $this->applyEditionMenuLabel();
        }

        // Task #462 — warn admins who carry a historical `license_tier='pro'`
        // but never installed the pkg_aiboost_pro package. With the manifest
        // codegen + STRICT verifier the Free build no longer silently exposes
        // Pro options, so these users would otherwise wonder where their
        // toggles went.
        $this->warnProLicenseWithoutProPackage();

        if (in_array($type, ['install', 'discover_install'], true)) {
            $this->showInstallMessage();
        } else {
            $this->showUpgradeMessage();
        }

        // Task #527 — installation integrity summary: confirm every expected
        // AI Boost extension installed/enabled and flag leftovers or version
        // mismatches. Uses the exact same audit as the Health tab check.
        $this->showIntegritySummary();
    }

    /**
     * Task #527 — Run the installation integrity audit and enqueue one concise
     * summary message. Informational on a clean result, a warning when anything
     * is missing/disabled/leftover/mismatched. Never blocks the install.
     */
    private function showIntegritySummary(): void
    {
        try {
            $db = Factory::getDbo();

            // The lib autoloader may not be registered yet during the first
            // package install, so require the just-installed file directly.
            if (!class_exists('AiBoost\\Lib\\InstallIntegrity', false)) {
                $file = JPATH_ADMINISTRATOR . '/components/com_aiboost/lib/src/InstallIntegrity.php';
                if (is_file($file)) {
                    require_once $file;
                }
            }
            if (!class_exists('AiBoost\\Lib\\InstallIntegrity', false)) {
                return;
            }

            // Resolve the authoritative package version from the installed
            // Version.php (self::VERSION on the script can lag the build).
            $version = self::VERSION;
            $vfile   = JPATH_ADMINISTRATOR . '/components/com_aiboost/Version.php';
            if (!class_exists('AiBoost\\Version', false) && is_file($vfile)) {
                require_once $vfile;
            }
            if (class_exists('AiBoost\\Version', false)) {
                $version = (string) \AiBoost\Version::VERSION;
            }

            $isPro = \AiBoost\Lib\InstallIntegrity::isProEdition($db);
            $audit = \AiBoost\Lib\InstallIntegrity::audit($db, $isPro, $version);
            $line  = \AiBoost\Lib\InstallIntegrity::summaryLine($audit);

            $hasProblem = !empty($audit['missing'])
                || !empty($audit['disabled'])
                || !empty($audit['orphan'])
                || !empty($audit['mismatch']);

            Factory::getApplication()->enqueueMessage(
                'AI Boost installation integrity: ' . $line
                . ($hasProblem
                    ? ' Open the AI Boost Health tab → Installation Integrity for details.'
                    : ''),
                $hasProblem ? 'warning' : 'message'
            );
        } catch (\Throwable $e) {
            self::logEvent('warning', '[AiBoost] showIntegritySummary failed: ' . $e->getMessage());
        }
    }

    /**
     * Task #462 — Detect "ghost Pro" installs: settings carry a historical
     * `license_tier='pro'` (set before per-SKU map landed in Task #428) but
     * the Pro package is not physically installed. Show an admin notice with
     * a download link instead of silently rendering as Free.
     */
    private function warnProLicenseWithoutProPackage(): void
    {
        try {
            $db = Factory::getDbo();

            $tables = $db->setQuery(
                'SHOW TABLES LIKE ' . $db->quote($db->getPrefix() . 'aiboost_settings')
            )->loadColumn();
            if (!in_array($db->getPrefix() . 'aiboost_settings', $tables, true)) {
                return;
            }

            $raw = (string) $db->setQuery(
                $db->getQuery(true)
                    ->select($db->quoteName('settings_json'))
                    ->from($db->quoteName('#__aiboost_settings'))
                    ->where($db->quoteName('setting_key') . ' = ' . $db->quote('main'))
            )->loadResult();
            if ($raw === '') {
                return;
            }
            $data = json_decode($raw, true) ?: [];
            $tier = strtolower((string) ($data['license_tier'] ?? 'free'));
            if (!in_array($tier, ['pro', 'developer', 'agency'], true)) {
                return;
            }

            // One-product model: "Pro" is the SAME pkg_aiboost package built with the
            // Pro code included (IS_PRO_EDITION = true) — there is no separate
            // pkg_aiboost_pro package any more. So only warn when a Pro licence is
            // active while this site is running the FREE (Pro-stripped) build; the fix
            // is to install the Pro build over it (settings + licence are preserved).
            if (self::IS_PRO_EDITION) {
                return;
            }

            Factory::getApplication()->enqueueMessage(
                'AI Boost: your licence is Pro, but this site is running the Free build, '
                . 'so Pro features stay locked. Download the Pro package from '
                . 'https://aiboostnow.com/account and install it over this site through '
                . 'Joomla → Install Extensions — your settings and licence are kept.',
                'warning'
            );
        } catch (\Throwable $e) {
            self::logEvent('warning', '[AiBoost] warnProLicenseWithoutProPackage failed: ' . $e->getMessage());
        }
    }

    /**
     * Load the shared OG custom-field catalogue (DRY with
     * SettingsController::repairOgFields() — order 0017, G9). pkg_script cannot
     * autoload the lib, so the file is required explicitly; returns [] when the
     * component lib is not yet on disk (caller degrades gracefully).
     *
     * @return list<array<string,mixed>>
     */
    private function loadOgFieldDefs(): array
    {
        if (!class_exists('AiBoost\\Lib\\OgCustomFieldCatalog', false)) {
            $file = JPATH_ADMINISTRATOR . '/components/com_aiboost/lib/src/OgCustomFieldCatalog.php';
            if (is_file($file)) {
                require_once $file;
            }
        }
        if (!class_exists('AiBoost\\Lib\\OgCustomFieldCatalog', false)) {
            return [];
        }
        return \AiBoost\Lib\OgCustomFieldCatalog::fields();
    }

    /**
     * Auto-create the 6 AI Boost OG custom fields on install and update.
     *
     * Logic (idempotent / non-destructive):
     *   - Field group "AI Boost — OpenGraph" is created once if absent.
     *   - Each field: INSERT if absent, UPDATE label/description/fieldparams/note if the
     *     version marker in the `note` column differs, SKIP if already at current version.
     *   - #__fields_values (user-entered article overrides) are NEVER touched.
     *   - Column presence (fieldparams, only_use_in_subform) is detected at runtime
     *     via SHOW COLUMNS so the script works across Joomla 5 and 6.
     */
    private function ensureOgCustomFields(): void
    {
        $context     = 'com_content.article';
        $groupTitle  = 'AI Boost — OpenGraph';
        $version     = self::VERSION;
        $diagnostics = [];

        // Single source of truth — shared with SettingsController::repairOgFields()
        // (order 0017, G9). pkg_script cannot autoload the lib, so require the
        // catalogue file explicitly (same pattern as InstallIntegrity/RobotsTxtBuilder).
        $fieldDefs = $this->loadOgFieldDefs();
        if ($fieldDefs === []) {
            $msg = '[AiBoost] OG custom-field catalogue unavailable — skipping field auto-create.';
            error_log($msg);
            Factory::getApplication()->enqueueMessage(
                'AI Boost: could not load the OG field catalogue; create OG fields via the '
                . '"Create / Repair OG Fields" button on the Social tab.',
                'warning'
            );
            return;
        }

        // ── Step 1: get/create group via Joomla stock model ──
        try {
            $groupId = $this->upsertFieldGroup($context, $groupTitle);
            $diagnostics[] = 'Group "' . $groupTitle . '" id=' . $groupId;
        } catch (\Throwable $e) {
            $msg = '[AiBoost] Group create failed: ' . $e->getMessage();
            error_log($msg);
            \Joomla\CMS\Log\Log::add($msg, \Joomla\CMS\Log\Log::ERROR, 'aiboost');
            Factory::getApplication()->enqueueMessage(
                'AI Boost: Could not create field group (' . $e->getMessage()
                . '). Create OG fields manually via Content -> Fields.',
                'warning'
            );
            return;
        }

        // ── Step 2: upsert each field ──
        $created = 0;
        $updated = 0;
        $skipped = 0;
        $failed  = 0;

        foreach ($fieldDefs as $def) {
            try {
                $result = $this->upsertField($context, (int) $groupId, $def, $version);
                $diagnostics[] = $def['name'] . ': ' . $result;
                match ($result) {
                    'created' => $created++,
                    'updated' => $updated++,
                    default   => $skipped++,
                };
            } catch (\Throwable $e) {
                $failed++;
                $errMsg = '[AiBoost] Field "' . $def['name'] . '" failed: ' . $e->getMessage();
                error_log($errMsg);
                \Joomla\CMS\Log\Log::add($errMsg, \Joomla\CMS\Log\Log::ERROR, 'aiboost');
                $diagnostics[] = $def['name'] . ': FAILED — ' . $e->getMessage();
            }
        }

        // ── Step 3: user-visible summary ──
        $msgType = $failed > 0 ? 'warning' : 'message';
        $summary = sprintf(
            'AI Boost: OG custom fields — %d created, %d updated, %d unchanged, %d failed.'
            . ' Visible under <em>Content -> Fields -> Articles</em> in "AI Boost — OpenGraph" group.',
            $created, $updated, $skipped, $failed
        );

        if ($failed > 0) {
            $summary .= '<br><small><strong>Diagnostic log:</strong><br>'
                . implode('<br>', array_map('htmlspecialchars', $diagnostics))
                . '</small>';
        }

        Factory::getApplication()->enqueueMessage($summary, $msgType);
    }

    /**
     * Task #454 — Is this install Pro-licensed?
     *
     * Pro determination at install-time:
     *   - pro_installed / pro_activated markers in #__aiboost_settings.settings_json
     *   - OR license_tier ∈ {pro, developer, agency} in #__aiboost_settings.settings_json
     *   - OR a pkg_aiboost_pro package row exists in #__extensions
     *
     * The last clause matters on a fresh install where the settings row
     * doesn't have a tier yet but Pro is being installed in the same session.
     */
    private function isProInstall(): bool
    {
        try {
            $db = Factory::getDbo();

            // 1. settings-based tier (most common — set after license activation)
            $tables = $db->setQuery(
                'SHOW TABLES LIKE ' . $db->quote($db->getPrefix() . 'aiboost_settings')
            )->loadColumn();
            if (in_array($db->getPrefix() . 'aiboost_settings', $tables, true)) {
                $raw = (string) $db->setQuery(
                    $db->getQuery(true)
                        ->select($db->quoteName('settings_json'))
                        ->from($db->quoteName('#__aiboost_settings'))
                        ->where($db->quoteName('setting_key') . ' = ' . $db->quote('main'))
                )->loadResult();
                if ($raw !== '') {
                    $data = json_decode($raw, true) ?: [];
                    // Phase 6 — the install-edition marker (set by the combined
                    // Pro package) + the perpetual activation flag. Either marks
                    // a Pro install even when no *_pro row / license_tier exists
                    // (e.g. a direct-Pro buyer who has not yet entered a key, or
                    // a collapsed Pro site where the *_pro rows were swept).
                    if ((string) ($data['pro_installed'] ?? '0') === '1'
                        || (string) ($data['pro_activated'] ?? '0') === '1') {
                        return true;
                    }
                    $tier = strtolower((string) ($data['license_tier'] ?? 'free'));
                    if (in_array($tier, ['pro', 'developer', 'agency'], true)) {
                        return true;
                    }
                }
            }

            // 2. Legacy split layout physically present: the old pkg_aiboost_pro
            //    add-on package row, OR any live aiboost_*_pro decorator plugin.
            $proRow = (int) $db->setQuery(
                $db->getQuery(true)
                    ->select('COUNT(*)')
                    ->from($db->quoteName('#__extensions'))
                    ->where($db->quoteName('type') . ' = ' . $db->quote('package'))
                    ->where($db->quoteName('element') . ' = ' . $db->quote('pkg_aiboost_pro'))
            )->loadResult();
            if ($proRow > 0) {
                return true;
            }
            if ($this->hasLiveProExtension()) {
                return true;
            }
        } catch (\Throwable $e) {
            // On any error, fail closed → Free (no Pro features installed).
            self::logEvent('warning', '[AiBoost] isProInstall() failed, defaulting to Free: ' . $e->getMessage());
        }
        return false;
    }

    /**
     * Phase 6.4 — the installed pkg_aiboost package version (from its
     * #__extensions manifest_cache), or '' when not installed / unreadable.
     */
    private function installedPackageVersion(): string
    {
        try {
            $db  = Factory::getDbo();
            $row = (string) $db->setQuery(
                $db->getQuery(true)
                    ->select($db->quoteName('manifest_cache'))
                    ->from($db->quoteName('#__extensions'))
                    ->where($db->quoteName('type') . ' = ' . $db->quote('package'))
                    ->where($db->quoteName('element') . ' = ' . $db->quote('pkg_aiboost'))
            )->loadResult();
            if ($row === '') {
                return '';
            }
            $cache = json_decode($row, true) ?: [];
            return trim((string) ($cache['version'] ?? ''));
        } catch (\Throwable $e) {
            return '';
        }
    }

    /**
     * Phase 6.4 — true when this is a paying/Pro install by FLAG (the install
     * marker or the perpetual activation flag). Gates ONLY the downgrade lock —
     * a genuine Free site is never blocked from installing anything.
     */
    private function isProSiteByFlags(): bool
    {
        $raw  = $this->readMainSettingsJson();
        $data = ($raw !== null && $raw !== '') ? json_decode($raw, true) : [];
        if (!is_array($data)) {
            return false;
        }
        return (string) ($data['pro_installed'] ?? '0') === '1'
            || (string) ($data['pro_activated'] ?? '0') === '1';
    }

    /**
     * Phase 6.5 — sweep the legacy *_pro decorator plugins + the old
     * pkg_aiboost_pro add-on package. The Pro logic was relocated into the 7
     * free plugins (the "Pro replaces Free" collapse), so these are dead weight;
     * a Pro site should show ONLY the 7 single elements. Runs ONLY from the
     * combined Pro-edition install (gated by IS_PRO_EDITION), AFTER the 7 core
     * plugins have upgraded in place, so no live logic is stripped.
     *
     * Cascade-safe: the old pkg_aiboost_pro set blockChildUninstall, so we NEVER
     * call Installer->uninstall() on it. Per child: detach from the parent
     * package (package_id=0, enabled=0) → row-delete the child rows; ONLY THEN
     * row-delete the package row directly. Any row that survives is logged AND
     * enqueued so a partial sweep is visible, never silent.
     */
    private function sweepCollapsedProDecorators(): void
    {
        $decorators = [
            'aiboost_schema_pro',
            'aiboost_aeo_pro',
            'aiboost_social_pro',
            'aiboost_hreflang_pro',
            'aiboost_code_pro',
        ];
        $survivors = [];

        try {
            $db = Factory::getDbo();

            foreach ($decorators as $element) {
                $ids = array_map('intval', (array) $db->setQuery(
                    $db->getQuery(true)
                        ->select($db->quoteName('extension_id'))
                        ->from($db->quoteName('#__extensions'))
                        ->where($db->quoteName('type') . ' = ' . $db->quote('plugin'))
                        ->where($db->quoteName('folder') . ' = ' . $db->quote('system'))
                        ->where($db->quoteName('element') . ' = ' . $db->quote($element))
                )->loadColumn());

                foreach ($ids as $extId) {
                    // (1) detach from the parent package so no cascade fires.
                    try {
                        $db->setQuery(
                            $db->getQuery(true)
                                ->update($db->quoteName('#__extensions'))
                                ->set($db->quoteName('package_id') . ' = 0')
                                ->set($db->quoteName('enabled') . ' = 0')
                                ->where($db->quoteName('extension_id') . ' = ' . (int) $extId)
                        )->execute();
                    } catch (\Throwable $e) {
                        // best-effort; the row-delete below still removes it.
                    }
                    // (2) row-delete the child (#__extensions + #__schemas).
                    $this->deleteLegacyExtensionRows($extId);
                }

                // (3) remove the orphaned plugin folder on disk (harmless if absent).
                $this->deleteProDecoratorFolder($element);

                $still = (int) $db->setQuery(
                    $db->getQuery(true)
                        ->select('COUNT(*)')
                        ->from($db->quoteName('#__extensions'))
                        ->where($db->quoteName('type') . ' = ' . $db->quote('plugin'))
                        ->where($db->quoteName('folder') . ' = ' . $db->quote('system'))
                        ->where($db->quoteName('element') . ' = ' . $db->quote($element))
                )->loadResult();
                if ($still > 0) {
                    $survivors[] = $element;
                }
            }

            // (4) ONLY NOW row-delete the old pkg_aiboost_pro package row directly
            //     — never via Installer->uninstall() (blockChildUninstall cascade).
            $pkgIds = array_map('intval', (array) $db->setQuery(
                $db->getQuery(true)
                    ->select($db->quoteName('extension_id'))
                    ->from($db->quoteName('#__extensions'))
                    ->where($db->quoteName('type') . ' = ' . $db->quote('package'))
                    ->where($db->quoteName('element') . ' = ' . $db->quote('pkg_aiboost_pro'))
            )->loadColumn());
            foreach ($pkgIds as $pid) {
                $this->deleteLegacyExtensionRows($pid);
            }
        } catch (\Throwable $e) {
            self::logEvent('warning', '[AiBoost] sweepCollapsedProDecorators failed: ' . $e->getMessage());
        }

        if ($survivors) {
            $list = implode(', ', $survivors);
            self::logEvent('warning', '[AiBoost] sweep: legacy Pro decorator row(s) survived: ' . $list);
            try {
                Factory::getApplication()->enqueueMessage(
                    'AI Boost: some legacy Pro plugin rows could not be removed (' . $list
                    . '). You can remove them under Extensions → Manage.',
                    'warning'
                );
            } catch (\Throwable $e) {
            }
        }
    }

