    /**
     * Task #482 — Migrate AI crawler rules to the new page-level default
     * policy model.
     *
     * Two-step backfill, idempotent and safe to re-run:
     *   1. If `aeo_crawler_default_policy` is missing, set it to 'allow'
     *      (preserves the prior "Default = follow global robots.txt"
     *      semantics — the section emitted no per-bot block at all, which
     *      effectively allowed every bot via robots.txt's User-agent: *).
     *   2. Rewrite any per-bot 'default' value in `crawler_bot_rules` to
     *      '' (empty) so the new radio UI starts unselected and the
     *      builder uses the page-level policy as the fallback.
     */
    private function migrateCrawlerDefaultPolicy(): void
    {
        try {
            $db = Factory::getDbo();
            $tables = $db->setQuery(
                'SHOW TABLES LIKE ' . $db->quote($db->getPrefix() . 'aiboost_settings')
            )->loadColumn();
            if (!in_array($db->getPrefix() . 'aiboost_settings', $tables, true)) {
                return;
            }

            $existingJson = $db->setQuery(
                $db->getQuery(true)
                    ->select($db->quoteName('settings_json'))
                    ->from($db->quoteName('#__aiboost_settings'))
                    ->where($db->quoteName('setting_key') . ' = ' . $db->quote('main'))
            )->loadResult();
            if ($existingJson === null) {
                return;
            }

            $data    = json_decode((string) $existingJson, true) ?? [];
            $changed = false;

            if (!array_key_exists('aeo_crawler_default_policy', $data)
                || $data['aeo_crawler_default_policy'] === ''
                || $data['aeo_crawler_default_policy'] === null) {
                $data['aeo_crawler_default_policy'] = 'allow';
                $changed = true;
            }

            if (array_key_exists('crawler_bot_rules', $data)) {
                $rules = json_decode((string) $data['crawler_bot_rules'], true);
                if (is_array($rules)) {
                    $rewritten = false;
                    // Only rewrite the legacy literal value 'default' (the
                    // dropdown option that no longer exists) → ''. Preserve
                    // 'allow', 'block', and legacy 'disallow' (which the
                    // builder still honours as block for backward compat).
                    foreach ($rules as $botId => $value) {
                        if (strtolower(trim((string) $value)) === 'default') {
                            $rules[$botId] = '';
                            $rewritten = true;
                        }
                    }
                    if ($rewritten) {
                        $data['crawler_bot_rules'] = json_encode($rules, JSON_UNESCAPED_SLASHES);
                        $changed = true;
                    }
                }
            }

            if (!$changed) {
                return;
            }

            $newJson = json_encode($data, JSON_UNESCAPED_SLASHES);
            $db->setQuery(
                $db->getQuery(true)
                    ->update($db->quoteName('#__aiboost_settings'))
                    ->set($db->quoteName('settings_json') . ' = ' . $db->quote($newJson))
                    ->where($db->quoteName('setting_key') . ' = ' . $db->quote('main'))
            )->execute();
        } catch (\Throwable $e) {
            self::logEvent('warning', '[AiBoost] migrateCrawlerDefaultPolicy failed: ' . $e->getMessage());
        }
    }

    private function generateUuidV4(): string
    {
        $bytes    = random_bytes(16);
        $bytes[6] = chr((ord($bytes[6]) & 0x0f) | 0x40);
        $bytes[8] = chr((ord($bytes[8]) & 0x3f) | 0x80);
        $hex = bin2hex($bytes);
        return sprintf(
            '%s-%s-%s-%s-%s',
            substr($hex, 0, 8),
            substr($hex, 8, 4),
            substr($hex, 12, 4),
            substr($hex, 16, 4),
            substr($hex, 20, 12)
        );
    }

