use Joomla\CMS\Factory;

class Pkg_AiboostInstallerScript
{
    public const VERSION     = '0.50.2';
    public const MIN_PHP     = '8.1.0';
    public const MIN_JOOMLA  = '5.0.0';

    /**
     * Task #566 — AI Boost owns ONLY a fenced block inside the user's
     * robots.txt. These markers MUST stay byte-identical with
     * AiBoost\Lib\RobotsTxtBuilder::{BEGIN,END}_MARKER and the legacy marker;
     * the installer/uninstaller cannot autoload the lib, so they are inlined.
     */
    private const ROBOTS_BEGIN_MARKER  = '# BEGIN AI Boost for Joomla managed block (aiboostnow.com) - do not edit between these markers';
    private const ROBOTS_END_MARKER    = '# END AI Boost for Joomla managed block';
    private const ROBOTS_LEGACY_MARKER = '# Managed by AI Boost';

    /**
     * Strip the AI Boost managed block from existing robots.txt content,
     * preserving any user-authored rules. Mirrors
     * AiBoost\Lib\RobotsTxtBuilder::stripManagedBlock(). Returns '' when the
     * whole file was ours (current fenced-only or legacy pre-fence format).
     */
    private function stripRobotsBlock(string $existing): string
    {
        if ($existing === '') {
            return '';
        }

        // Legacy pre-fence install: the entire file was generated by us.
        if (str_starts_with(ltrim($existing, "\xEF\xBB\xBF \t\r\n"), self::ROBOTS_LEGACY_MARKER)) {
            return '';
        }

        $pattern = '/\n*' . preg_quote(self::ROBOTS_BEGIN_MARKER, '/')
            . '.*?' . preg_quote(self::ROBOTS_END_MARKER, '/') . '[^\n]*\n?/su';

        return (string) preg_replace($pattern, '', $existing);
    }

    /**
     * Task #511 — Route installer log events through the central Logger
     * when the AiBoost\Lib autoloader is already registered. During the
     * very first package install the lib autoloader is not yet wired in,
     * so fall back to native error_log() and keep the install path safe.
     */
    private static function logEvent(string $severity, string $msg): void
    {
        if (class_exists('AiBoost\\Lib\\Logger', false)) {
            try {
                \AiBoost\Lib\Logger::$severity($msg, ['source' => 'pkg_script']);
                return;
            } catch (\Throwable $e) {
                // fall through to error_log
            }
        }
        @error_log('[AiBoost][' . strtoupper($severity) . '] ' . $msg);
    }

    public function preflight(string $type, object $parent): bool
    {
        if (version_compare(PHP_VERSION, self::MIN_PHP, '<')) {
            Factory::getApplication()->enqueueMessage(
                sprintf('AI Boost requires PHP %s or higher. You are running PHP %s.', self::MIN_PHP, PHP_VERSION),
                'error'
            );
            return false;
        }

        if (version_compare(JVERSION, self::MIN_JOOMLA, '<')) {
            Factory::getApplication()->enqueueMessage(
                sprintf('AI Boost requires Joomla %s or higher. You are running Joomla %s.', self::MIN_JOOMLA, JVERSION),
                'error'
            );
            return false;
        }

        return true;
    }

    public function postflight(string $type, object $parent): void
    {
        // v0.7.0 — ensure new tables exist on upgrade
        $this->ensureNewTables();

        // Migration runs on both install and upgrade:
        //   • On upgrade from pkg_aiboost: idempotent upsert skips already-migrated rows.
        //   • On fresh install after running plg_system_joomlaboost: old tables are present
        //     and must be migrated even though this is technically an 'install' of the new
        //     package (users uninstall the old plugin ZIP, then install the new package).
        $this->runDataMigration();

        // v0.32.5 — migrate legacy aggregate `robots_block_scrapers` → per-bot scraper_* keys.
        // MUST run AFTER runDataMigration() so that rows copied from the legacy
        // #__joomlaboost_settings table are also canonicalized in the same postflight pass.
        $this->migrateRobotsBlockScrapers();
        $this->migrateSchemaAuthorEntity();
        // Task #428 — migrate legacy `license_tier` flag into per-SKU map.
        $this->migrateLicenseTierToProSkuMap();
        // Task #565 — perpetual activation backfill (MUST run AFTER the #428
        // migration so an active license_state is already materialised).
        $this->migrateActivateProPerpetual();
        // Task #438 — uninstall legacy aiboost_perf plugin (renamed to aiboost_core).
        $this->migrateAiboostPerfToCore();
        // Task #440 — ensure a stable install_id (UUIDv4) exists so the
        // license-heartbeat endpoint can detect domain/install collisions.
        $this->ensureInstallId();
        // Task #473 — default conflict_mode to "cooperative" so existing
        // installs adopt the new ConflictDetector-friendly behaviour.
        $this->ensureConflictModeDefault();
        // Task #482 — backfill page-level default policy and rewrite legacy
        // per-bot 'default' values to '' so the new radio UI is unambiguous.
        $this->migrateCrawlerDefaultPolicy();
        $this->enablePlugins();

        if (in_array($type, ['install', 'update', 'discover_install'], true)) {
            // Task #566 — (re)generate static artifacts (robots.txt managed
            // block, llms.txt, sitemap) ONLY on a genuine install/update.
            // Calling this unconditionally re-injected the robots.txt block
            // during an uninstall-context postflight, defeating the uninstall
            // strip.
            $this->cleanupStaticFiles();

            // Task #454 — Article OG custom fields are a Pro feature.
            // On Free installs we (a) skip the field upsert and (b) remove any
            // rows we previously installed so the Free admin's article editor
            // is clean. #__fields_values (user-entered overrides) are NEVER
            // touched — they remain in the DB and reappear if the user later
            // upgrades to Pro.
            if ($this->isProInstall()) {
                $this->ensureOgCustomFields();
            } else {
                $this->removeOgCustomFieldsForFree();
                // The admin Health module is a Pro-only surface (it now ships in
                // pkg_aiboost_pro and is published by pkg_script_pro). On a Free
                // install we actively remove any instance + the module extension
                // that older base packages used to ship, so Free has no module.
                $this->removeHealthModuleForFree();
            }

            // Task #455 — relabel the Components → AI Boost menu entry so
            // admins see at a glance whether they're on Free or Pro.
            $this->applyEditionMenuLabel();
        }

        // Task #462 — warn admins who carry a historical `license_tier='pro'`
        // but never installed the pkg_aiboost_pro package. With the manifest
        // codegen + STRICT verifier the Free build no longer silently exposes
        // Pro options, so these users would otherwise wonder where their
        // toggles went.
        $this->warnProLicenseWithoutProPackage();

        if (in_array($type, ['install', 'discover_install'], true)) {
            $this->showInstallMessage();
        } else {
            $this->showUpgradeMessage();
        }

        // Task #527 — installation integrity summary: confirm every expected
        // AI Boost extension installed/enabled and flag leftovers or version
        // mismatches. Uses the exact same audit as the Health tab check.
        $this->showIntegritySummary();
    }

    /**
     * Task #527 — Run the installation integrity audit and enqueue one concise
     * summary message. Informational on a clean result, a warning when anything
     * is missing/disabled/leftover/mismatched. Never blocks the install.
     */
    private function showIntegritySummary(): void
    {
        try {
            $db = Factory::getDbo();

            // The lib autoloader may not be registered yet during the first
            // package install, so require the just-installed file directly.
            if (!class_exists('AiBoost\\Lib\\InstallIntegrity', false)) {
                $file = JPATH_ADMINISTRATOR . '/components/com_aiboost/lib/src/InstallIntegrity.php';
                if (is_file($file)) {
                    require_once $file;
                }
            }
            if (!class_exists('AiBoost\\Lib\\InstallIntegrity', false)) {
                return;
            }

            // Resolve the authoritative package version from the installed
            // Version.php (self::VERSION on the script can lag the build).
            $version = self::VERSION;
            $vfile   = JPATH_ADMINISTRATOR . '/components/com_aiboost/Version.php';
            if (!class_exists('AiBoost\\Version', false) && is_file($vfile)) {
                require_once $vfile;
            }
            if (class_exists('AiBoost\\Version', false)) {
                $version = (string) \AiBoost\Version::VERSION;
            }

            $isPro = \AiBoost\Lib\InstallIntegrity::isProEdition($db);
            $audit = \AiBoost\Lib\InstallIntegrity::audit($db, $isPro, $version);
            $line  = \AiBoost\Lib\InstallIntegrity::summaryLine($audit);

            $hasProblem = !empty($audit['missing'])
                || !empty($audit['disabled'])
                || !empty($audit['orphan'])
                || !empty($audit['mismatch']);

            Factory::getApplication()->enqueueMessage(
                'AI Boost installation integrity: ' . $line
                . ($hasProblem
                    ? ' Open the AI Boost Health tab → Installation Integrity for details.'
                    : ''),
                $hasProblem ? 'warning' : 'message'
            );
        } catch (\Throwable $e) {
            self::logEvent('warning', '[AiBoost] showIntegritySummary failed: ' . $e->getMessage());
        }
    }

    /**
     * Task #462 — Detect "ghost Pro" installs: settings carry a historical
     * `license_tier='pro'` (set before per-SKU map landed in Task #428) but
     * the Pro package is not physically installed. Show an admin notice with
     * a download link instead of silently rendering as Free.
     */
    private function warnProLicenseWithoutProPackage(): void
    {
        try {
            $db = Factory::getDbo();

            $tables = $db->setQuery(
                'SHOW TABLES LIKE ' . $db->quote($db->getPrefix() . 'aiboost_settings')
            )->loadColumn();
            if (!in_array($db->getPrefix() . 'aiboost_settings', $tables, true)) {
                return;
            }

            $raw = (string) $db->setQuery(
                $db->getQuery(true)
                    ->select($db->quoteName('settings_json'))
                    ->from($db->quoteName('#__aiboost_settings'))
                    ->where($db->quoteName('setting_key') . ' = ' . $db->quote('main'))
            )->loadResult();
            if ($raw === '') {
                return;
            }
            $data = json_decode($raw, true) ?: [];
            $tier = strtolower((string) ($data['license_tier'] ?? 'free'));
            if (!in_array($tier, ['pro', 'developer', 'agency'], true)) {
                return;
            }

            // Pro package physically installed?
            $proRow = (int) $db->setQuery(
                $db->getQuery(true)
                    ->select('COUNT(*)')
                    ->from($db->quoteName('#__extensions'))
                    ->where($db->quoteName('type') . ' = ' . $db->quote('package'))
                    ->where($db->quoteName('element') . ' = ' . $db->quote('pkg_aiboost_pro'))
            )->loadResult();
            if ($proRow > 0) {
                return;
            }

            Factory::getApplication()->enqueueMessage(
                'AI Boost: your settings record a Pro licence but the Pro '
                . 'package (pkg_aiboost_pro) is not installed on this site. '
                . 'Pro options are now packaged separately. '
                . 'Download Pro from https://aiboostnow.com/account and install '
                . 'the pkg_aiboost_pro ZIP through Joomla → Install Extensions.',
                'warning'
            );
        } catch (\Throwable $e) {
            self::logEvent('warning', '[AiBoost] warnProLicenseWithoutProPackage failed: ' . $e->getMessage());
        }
    }

    /**
     * Auto-create the 6 AI Boost OG custom fields on install and update.
     *
     * Logic (idempotent / non-destructive):
     *   - Field group "AI Boost — OpenGraph" is created once if absent.
     *   - Each field: INSERT if absent, UPDATE label/description/fieldparams/note if the
     *     version marker in the `note` column differs, SKIP if already at current version.
     *   - #__fields_values (user-entered article overrides) are NEVER touched.
     *   - Column presence (fieldparams, only_use_in_subform) is detected at runtime
     *     via SHOW COLUMNS so the script works across Joomla 5 and 6.
     */
    private function ensureOgCustomFields(): void
    {
        $context     = 'com_content.article';
        $groupTitle  = 'AI Boost — OpenGraph';
        $version     = self::VERSION;
        $diagnostics = [];

        $fieldDefs = [
            [
                'name'        => 'aiboost_og_title',
                'title'       => 'AI Boost — OG Title',
                'type'        => 'text',
                'description' => 'Override the og:title meta tag. Leave empty to use the article title.',
                'fieldparams' => [],
                'ordering'    => 1,
            ],
            [
                'name'        => 'aiboost_og_description',
                'title'       => 'AI Boost — OG Description',
                'type'        => 'textarea',
                'description' => 'Override the og:description meta tag for this article.',
                'fieldparams' => ['rows' => '3', 'cols' => ''],
                'ordering'    => 2,
            ],
            [
                'name'        => 'aiboost_og_image',
                'title'       => 'AI Boost — OG Image',
                'type'        => 'media',
                'description' => 'Override og:image. Recommended size: 1200x630 px.',
                'fieldparams' => ['directory' => '', 'preview' => 'true'],
                'ordering'    => 3,
            ],
            [
                'name'        => 'aiboost_og_type',
                'title'       => 'AI Boost — OG Type',
                'type'        => 'list',
                'description' => 'Override the og:type meta tag. Defaults to "article" for article pages.',
                'fieldparams' => ['options' => [
                    ['name' => '— default (article) —', 'value' => ''],
                    ['name' => 'Article',               'value' => 'article'],
                    ['name' => 'Website',               'value' => 'website'],
                    ['name' => 'Video',                 'value' => 'video.movie'],
                    ['name' => 'Music',                 'value' => 'music.song'],
                    ['name' => 'Product',               'value' => 'product'],
                ]],
                'ordering'    => 4,
            ],
            [
                'name'        => 'aiboost_og_video',
                'title'       => 'AI Boost — OG Video URL',
                'type'        => 'url',
                'description' => 'Optional og:video URL. Enables video preview cards on Facebook and LinkedIn.',
                'fieldparams' => [],
                'ordering'    => 5,
            ],
            [
                'name'        => 'aiboost_twitter_card',
                'title'       => 'AI Boost — Twitter Card',
                'type'        => 'list',
                'description' => 'Override the twitter:card type. Defaults to summary_large_image.',
                'fieldparams' => ['options' => [
                    ['name' => '— default (summary_large_image) —', 'value' => ''],
                    ['name' => 'Summary Large Image',               'value' => 'summary_large_image'],
                    ['name' => 'Summary',                           'value' => 'summary'],
                ]],
                'ordering'    => 6,
            ],
        ];

        // ── Step 1: get/create group via Joomla stock model ──
        try {
            $groupId = $this->upsertFieldGroup($context, $groupTitle);
            $diagnostics[] = 'Group "' . $groupTitle . '" id=' . $groupId;
        } catch (\Throwable $e) {
            $msg = '[AiBoost] Group create failed: ' . $e->getMessage();
            error_log($msg);
            \Joomla\CMS\Log\Log::add($msg, \Joomla\CMS\Log\Log::ERROR, 'aiboost');
            Factory::getApplication()->enqueueMessage(
                'AI Boost: Could not create field group (' . $e->getMessage()
                . '). Create OG fields manually via Content -> Fields.',
                'warning'
            );
            return;
        }

        // ── Step 2: upsert each field ──
        $created = 0;
        $updated = 0;
        $skipped = 0;
        $failed  = 0;

        foreach ($fieldDefs as $def) {
            try {
                $result = $this->upsertField($context, (int) $groupId, $def, $version);
                $diagnostics[] = $def['name'] . ': ' . $result;
                match ($result) {
                    'created' => $created++,
                    'updated' => $updated++,
                    default   => $skipped++,
                };
            } catch (\Throwable $e) {
                $failed++;
                $errMsg = '[AiBoost] Field "' . $def['name'] . '" failed: ' . $e->getMessage();
                error_log($errMsg);
                \Joomla\CMS\Log\Log::add($errMsg, \Joomla\CMS\Log\Log::ERROR, 'aiboost');
                $diagnostics[] = $def['name'] . ': FAILED — ' . $e->getMessage();
            }
        }

        // ── Step 3: user-visible summary ──
        $msgType = $failed > 0 ? 'warning' : 'message';
        $summary = sprintf(
            'AI Boost: OG custom fields — %d created, %d updated, %d unchanged, %d failed.'
            . ' Visible under <em>Content -> Fields -> Articles</em> in "AI Boost — OpenGraph" group.',
            $created, $updated, $skipped, $failed
        );

        if ($failed > 0) {
            $summary .= '<br><small><strong>Diagnostic log:</strong><br>'
                . implode('<br>', array_map('htmlspecialchars', $diagnostics))
                . '</small>';
        }

        Factory::getApplication()->enqueueMessage($summary, $msgType);
    }

    /**
     * Task #454 — Is this install Pro-licensed?
     *
     * Pro determination at install-time mirrors Dashboard::checkIsProEnabled():
     *   - license_tier ∈ {pro, developer, agency} in #__aiboost_settings.settings_json
     *   - OR dev_license_preview === '1' (developer simulator)
     *   - OR a pkg_aiboost_pro package row exists in #__extensions
     *
     * The third clause matters on a fresh install where the settings row
     * doesn't have a tier yet but Pro is being installed in the same session.
     */
    private function isProInstall(): bool
    {
        try {
            $db = Factory::getDbo();

            // 1. settings-based tier (most common — set after license activation)
            $tables = $db->setQuery(
                'SHOW TABLES LIKE ' . $db->quote($db->getPrefix() . 'aiboost_settings')
            )->loadColumn();
            if (in_array($db->getPrefix() . 'aiboost_settings', $tables, true)) {
                $raw = (string) $db->setQuery(
                    $db->getQuery(true)
                        ->select($db->quoteName('settings_json'))
                        ->from($db->quoteName('#__aiboost_settings'))
                        ->where($db->quoteName('setting_key') . ' = ' . $db->quote('main'))
                )->loadResult();
                if ($raw !== '') {
                    $data = json_decode($raw, true) ?: [];
                    $tier = strtolower((string) ($data['license_tier'] ?? 'free'));
                    if (in_array($tier, ['pro', 'developer', 'agency'], true)) {
                        return true;
                    }
                    if ((string) ($data['dev_license_preview'] ?? '0') === '1') {
                        return true;
                    }
                }
            }

            // 2. Pro package physically installed in #__extensions
            $proRow = (int) $db->setQuery(
                $db->getQuery(true)
                    ->select('COUNT(*)')
                    ->from($db->quoteName('#__extensions'))
                    ->where($db->quoteName('type') . ' = ' . $db->quote('package'))
                    ->where($db->quoteName('element') . ' = ' . $db->quote('pkg_aiboost_pro'))
            )->loadResult();
            if ($proRow > 0) {
                return true;
            }
        } catch (\Throwable $e) {
            // On any error, fail closed → Free (no Pro features installed).
            self::logEvent('warning', '[AiBoost] isProInstall() failed, defaulting to Free: ' . $e->getMessage());
        }
        return false;
    }

    /**
     * Task #454 — Remove AI Boost OG custom fields + their group from a
     * Free install.
     *
     * Ownership is verified BOTH ways:
     *   - fields must carry our ownership marker `note LIKE 'aiboost_version:%'`
     *     (written by upsertField()), in addition to matching our context +
     *     known field name. This prevents collateral damage if a third party
     *     happens to use a colliding field name.
     *   - the group must have `description LIKE 'AI Boost%'`.
     *
     * `#__fields_values` rows for the deleted field IDs are explicitly
     * removed (NOT preserved). Joomla generates new field IDs on re-create,
     * so any preserved values would orphan or rebind to the wrong field on a
     * later Pro reinstall — better to delete cleanly so a later Pro install
     * starts from a known empty state. Overrides on Free were a packaging
     * leak that should not have existed in the first place.
     *
     * Idempotent + silent no-op when the group/fields aren't present.
     */
    private function removeOgCustomFieldsForFree(): void
    {
        $context    = 'com_content.article';
        $groupTitle = 'AI Boost — OpenGraph';
        $fieldNames = [
            'aiboost_og_title', 'aiboost_og_description', 'aiboost_og_image',
            'aiboost_og_type', 'aiboost_og_video', 'aiboost_twitter_card',
        ];

        $removedFields = 0;
        $removedGroups = 0;
        $removedValues = 0;

        try {
            $db = Factory::getDbo();

            // Find our group (description marker = our installer)
            $groupId = (int) $db->setQuery(
                $db->getQuery(true)
                    ->select($db->quoteName('id'))
                    ->from($db->quoteName('#__fields_groups'))
                    ->where($db->quoteName('context') . ' = ' . $db->quote($context))
                    ->where($db->quoteName('title') . ' = ' . $db->quote($groupTitle))
                    ->where($db->quoteName('description') . ' LIKE ' . $db->quote('AI Boost%'))
            )->loadResult();

            // Only fields carrying our ownership marker `note LIKE 'aiboost_version:%'`
            // are candidates for deletion — exact name match alone is not enough.
            $fieldsQuoted = implode(',', array_map([$db, 'quote'], $fieldNames));
            $fieldIds = $db->setQuery(
                $db->getQuery(true)
                    ->select($db->quoteName('id'))
                    ->from($db->quoteName('#__fields'))
                    ->where($db->quoteName('context') . ' = ' . $db->quote($context))
                    ->where($db->quoteName('name') . ' IN (' . $fieldsQuoted . ')')
                    ->where($db->quoteName('note') . ' LIKE ' . $db->quote('aiboost_version:%'))
            )->loadColumn() ?: [];

            // Drop per-article values for our fields BEFORE deleting the field
            // rows. Field IDs change on re-create, so keeping these rows would
            // produce orphans (or rebind to a wrong, foreign field).
            if (!empty($fieldIds)) {
                $idsList = implode(',', array_map('intval', $fieldIds));
                try {
                    $db->setQuery(
                        'DELETE FROM ' . $db->quoteName('#__fields_values')
                        . ' WHERE ' . $db->quoteName('field_id') . ' IN (' . $idsList . ')'
                    )->execute();
                    $removedValues = (int) $db->getAffectedRows();
                } catch (\Throwable $inner) {
                    self::logEvent('warning', '[AiBoost] Could not delete OG field values: ' . $inner->getMessage());
                }
            }

            foreach ($fieldIds as $fieldId) {
                try {
                    $model = Factory::getApplication()->bootComponent('com_fields')
                        ->getMVCFactory()
                        ->createModel('Field', 'Administrator', ['ignore_request' => true]);
                    if ($model->delete($fieldId)) {
                        $removedFields++;
                    }
                } catch (\Throwable $inner) {
                    self::logEvent('warning', '[AiBoost] Could not delete OG field id=' . $fieldId . ': ' . $inner->getMessage());
                }
            }

            // Delete the now-empty group only if its description marker matched
            // ours (already enforced in the SELECT above; $groupId > 0 implies
            // verified ownership).
            if ($groupId > 0) {
                try {
                    $gModel = Factory::getApplication()->bootComponent('com_fields')
                        ->getMVCFactory()
                        ->createModel('Group', 'Administrator', ['ignore_request' => true]);
                    if ($gModel->delete($groupId)) {
                        $removedGroups++;
                    }
                } catch (\Throwable $inner) {
                    self::logEvent('warning', '[AiBoost] Could not delete OG group id=' . $groupId . ': ' . $inner->getMessage());
                }
            }

            if ($removedFields > 0 || $removedGroups > 0) {
                Factory::getApplication()->enqueueMessage(
                    sprintf(
                        'AI Boost (Free): removed %d Pro article custom field(s), %d group(s), and %d stored value(s). '
                        . 'Article OG overrides are a Pro feature — upgrade to Pro to re-enable them.',
                        $removedFields,
                        $removedGroups,
                        $removedValues
                    ),
                    'message'
                );
            }
        } catch (\Throwable $e) {
            self::logEvent('warning', '[AiBoost] removeOgCustomFieldsForFree failed: ' . $e->getMessage());
        }
    }

    /**
     * Task #455 — Set the admin Components → AI Boost menu label to reflect
     * the installed edition: "AI Boost Free" (Free) or "AI Boost Pro" (Pro).
     *
     * Joomla renders #__menu.title through Text::_(), so we store a language
     * key (COM_AIBOOST_MENU_FREE / COM_AIBOOST_MENU_PRO) — both defined in
     * com_aiboost.sys.ini — rather than literal text. The component slug
     * (option=com_aiboost), alias, and link all stay untouched, so existing
     * bookmarks, ACL rules, and routes keep working.
     *
     * Idempotent: re-running on the same edition is a no-op. Switching tiers
     * (e.g. installing Pro after Free, or letting Pro lapse) flips the label
     * automatically on the next package install/update.
     */
    private function applyEditionMenuLabel(): void
    {
        $newTitle = $this->isProInstall() ? 'COM_AIBOOST_MENU_PRO' : 'COM_AIBOOST_MENU_FREE';

        try {
            $db = Factory::getDbo();

            // Tight predicate: only the top-level admin Components → AI Boost
            // entry. We require client_id=1 (admin), type='component' (the
            // root component menu row, not a submenu/url/separator), a link
            // that targets com_aiboost, and a title equal to one of OUR
            // language keys. Admin-customized labels (anything not in this
            // exact set) are preserved untouched.
