    /**
     * Uninstall: PRESERVE all user data. We deliberately do NOT drop any
     * #__aiboost_* table — a user who accidentally removes the component and
     * reinstalls keeps every setting, redirect, translation, scan and log row.
     * The only state we reset is the licence: the six licence/dev keys are
     * removed (in PHP, read-modify-write) from the `main` #__aiboost_settings
     * row so a reinstall starts with a clean licence state while all
     * configuration and data stay intact.
     *
     * We still remove the front-of-site artifacts we generated (robots.txt
     * managed block, llms.txt, generated sitemap files). robots.txt is only
     * deleted when the file still carries our "Managed by AI Boost" header
     * marker so we never wipe a hand-edited file owned by the admin.
     *
     * Article custom fields: the same ownership-checked cleanup used by
     * removeOgCustomFieldsForFree() is invoked here, so the 6 aiboost_og_*
     * fields, the "AI Boost — OpenGraph" group, and any stored per-article
     * overrides are removed cleanly. The ownership predicate
     * (note LIKE 'aiboost_version:%') ensures third-party fields that
     * happen to share a name are never touched.
     *
     * Media folder (media/com_aiboost/): removed automatically by Joomla's
     * component uninstaller via the <media folder="media" destination=
     * "com_aiboost"> declaration in com_aiboost.xml — no explicit cleanup
     * needed here.
     */
    public function uninstall(object $parent): void
    {
        $db     = Factory::getDbo();
        $errors = [];

        // Article OG custom fields + per-article overrides. Safe to call on
        // every uninstall — the method only deletes rows carrying our own
        // ownership marker.
        try {
            $this->removeOgCustomFieldsForFree();
        } catch (\Throwable $e) {
            self::logEvent('warning', '[AiBoost] Uninstall: OG custom fields cleanup failed: ' . $e->getMessage());
        }

        // Reset ONLY the licence state — never the user's data. Remove the six
        // licence/dev keys from the `main` settings row in PHP (read-modify-write)
        // so a reinstall comes up unlicensed (clean licence state) while every
        // other setting survives. All #__aiboost_* tables are intentionally left
        // in place.
        //
        // We deliberately do NOT use the SQL JSON_REMOVE() function here:
        // settings_json is a NOT NULL MEDIUMTEXT column, and JSON_REMOVE() returns
        // SQL NULL on any parser hiccup. Writing NULL to a NOT NULL column is
        // coerced to '' under a non-strict sql_mode — which a reinstall then reads
        // as an empty settings blob and silently wipes ALL user data. Doing the
        // edit in PHP keeps the round-trip identical to every other helper here.
        $licenceKeys = [
            'license_key',
            'license_email',
            'pro_activated',
            'install_id',
            'dev_license_preview',
            'dev_force_free_tier',
        ];
        try {
            $existingJson = $db->setQuery(
                $db->getQuery(true)
                    ->select($db->quoteName('settings_json'))
                    ->from($db->quoteName('#__aiboost_settings'))
                    ->where($db->quoteName('setting_key') . ' = ' . $db->quote('main'))
            )->loadResult();

            // No row, or an already-empty blob — nothing to reset.
            if (is_string($existingJson) && $existingJson !== '') {
                $data = json_decode($existingJson, true);

                // If the stored value will not parse as a JSON object we leave it
                // ENTIRELY untouched. Overwriting unparseable-but-present settings
                // would lose the user's data — exactly what this task forbids.
                if (is_array($data)) {
                    $changed = false;
                    foreach ($licenceKeys as $k) {
                        if (array_key_exists($k, $data)) {
                            unset($data[$k]);
                            $changed = true;
                        }
                    }

                    if ($changed) {
                        // Encode as an object even when empty so the column never
                        // round-trips as a JSON array ("[]"). The substitute/partial
                        // flags keep a stray bad byte in some unrelated value from
                        // turning the whole encode into `false`.
                        $newJson = $data === []
                            ? '{}'
                            : json_encode(
                                $data,
                                JSON_UNESCAPED_SLASHES
                                | JSON_INVALID_UTF8_SUBSTITUTE
                                | JSON_PARTIAL_OUTPUT_ON_ERROR
                            );

                        // HARD SAFETY: never write an empty/false value. A NULL or
                        // '' here would be read by the next install as "no settings"
                        // and silently wipe ALL user data (the original bug, whether
                        // via SQL JSON_REMOVE→NULL or a failed json_encode→false).
                        // If we cannot produce a non-empty JSON object we abort the
                        // licence reset and keep the row exactly as it was.
                        if (is_string($newJson) && $newJson !== '' && $newJson !== '[]') {
                            $db->setQuery(
                                $db->getQuery(true)
                                    ->update($db->quoteName('#__aiboost_settings'))
                                    ->set($db->quoteName('settings_json') . ' = ' . $db->quote($newJson))
                                    ->where($db->quoteName('setting_key') . ' = ' . $db->quote('main'))
                            )->execute();
                        } else {
                            $errors[] = 'licence wipe';
                            self::logEvent('warning', '[AiBoost] Uninstall: skipped licence reset — re-encoding settings_json yielded an empty/invalid value; user data left intact.');
                        }
                    }
                } else {
                    self::logEvent('warning', '[AiBoost] Uninstall: settings_json for `main` did not decode to an array; left untouched to avoid data loss.');
                }
            }
        } catch (\Throwable $e) {
            $errors[] = 'licence wipe';
            self::logEvent('warning', '[AiBoost] Uninstall: could not wipe licence keys from #__aiboost_settings: ' . $e->getMessage());
        }

        // Remove generated static artifacts. Each removal is independently
        // guarded so one failure (e.g. permissions) does not abort the rest.
        $removedFiles = [];
        $marker       = 'Managed by AI Boost';

        // Task #566 — robots.txt is the user's file: strip ONLY our fenced
        // block. Delete the file outright only when it was entirely ours
        // (fenced-only or legacy pre-fence format → nothing left after strip).
        $robotsPath = JPATH_ROOT . '/robots.txt';
        if (is_file($robotsPath)) {
            $existing = (string) @file_get_contents($robotsPath);
            $stripped = $this->stripRobotsBlock($existing);
            if ($stripped !== $existing) {
                if (trim($stripped) === '') {
                    if (@unlink($robotsPath)) {
                        $removedFiles[] = 'robots.txt';
                    }
                } elseif (@file_put_contents($robotsPath, rtrim($stripped) . "\n") !== false) {
                    $removedFiles[] = 'robots.txt (managed block)';
                }
            }
        }

        foreach (['llms.txt', 'sitemap.xml', 'sitemap-index.xml'] as $file) {
            $path = JPATH_ROOT . '/' . $file;
            if (is_file($path)) {
                $head = (string) @file_get_contents($path, false, null, 0, 256);
                // sitemap.xml: only delete if generated by us (XML comment marker).
                $isOurs = $file === 'llms.txt'
                    ? true
                    : str_contains($head, $marker) || str_contains($head, 'aiboost')
                        || str_contains($head, 'AI Boost');
                if ($isOurs && @unlink($path)) {
                    $removedFiles[] = $file;
                }
            }
        }

        $msgParts = [];
        if (!$errors) {
            $msgParts[] = 'AI Boost package removed. Your data has been preserved '
                . '— all settings, redirects, translations and logs remain in the '
                . 'database, so reinstalling restores everything. Only the licence '
                . 'keys were cleared.';
        } else {
            $msgParts[] = 'AI Boost removed and your data preserved, but the '
                . 'following step(s) could not be completed automatically: '
                . implode(', ', $errors);
        }
        if ($removedFiles) {
            $msgParts[] = 'Removed generated files from site root: '
                . implode(', ', $removedFiles) . '.';
        }

        Factory::getApplication()->enqueueMessage(
            implode(' ', $msgParts),
            $errors ? 'warning' : 'message'
        );
    }
}
