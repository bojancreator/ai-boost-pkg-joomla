    private function migrateSettings(\Joomla\Database\DatabaseInterface $db): void
    {
        $prefix         = $db->getPrefix();
        $oldTable       = $prefix . 'joomlaboost_settings';
        $oldTableExists = false;

        try {
            $tables         = $db->setQuery('SHOW TABLES LIKE ' . $db->quote($oldTable))->loadColumn();
            $oldTableExists = in_array($oldTable, $tables, true);
        } catch (\Throwable $e) {
            self::logEvent('warning', '[AiBoost] Could not check for old settings table: ' . $e->getMessage());
        }

        if (!$oldTableExists) {
            return;
        }

        // Migrate ALL rows — merge into new table (upsert: skip rows whose key already exists)
        $oldRows = $db->setQuery('SELECT * FROM ' . $db->quoteName($oldTable))->loadObjectList();
        $now     = Factory::getDate()->toSql();
        $copied  = 0;
        $skipped = 0;
        $errors  = 0;

        foreach ($oldRows as $row) {
            $key = $row->setting_key ?? 'main';

            // Check if this key already exists in new table
            $exists = (int) $db->setQuery(
                $db->getQuery(true)->select('COUNT(*)')->from('#__aiboost_settings')
                    ->where($db->quoteName('setting_key') . '=' . $db->quote($key))
            )->loadResult();

            if ($exists > 0) {
                $skipped++;
                continue;
            }

            try {
                $db->setQuery(
                    $db->getQuery(true)
                        ->insert('#__aiboost_settings')
                        ->columns(['setting_key', 'settings_json', 'created_at', 'updated_at'])
                        ->values(
                            $db->quote($key) . ',' .
                            $db->quote($row->settings_json ?? '{}') . ',' .
                            $db->quote($now) . ',' .
                            $db->quote($now)
                        )
                )->execute();
                $copied++;
            } catch (\Throwable $e) {
                $errors++;
                self::logEvent('warning', '[AiBoost] Migration error (settings row ' . $key . '): ' . $e->getMessage());
            }
        }

        if ($errors === 0) {
            $this->dropOldTable($db, $oldTable);
            Factory::getApplication()->enqueueMessage(
                sprintf(
                    'AI Boost: Settings migration complete — %d row(s) copied, %d skipped (already existed). Old table removed.',
                    $copied,
                    $skipped
                ),
                'message'
            );
        } else {
            Factory::getApplication()->enqueueMessage(
                sprintf(
                    'AI Boost: Settings migration — %d copied, %d skipped, %d error(s). Old table kept for safety; check error log.',
                    $copied,
                    $skipped,
                    $errors
                ),
                'warning'
            );
        }
    }

